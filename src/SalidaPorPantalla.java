public class SalidaPorPantalla {
    public static void main(String[] args) {
        // Para imprimir cada cosa en una línea diferente
        System.out.println("Primera línea");
        System.out.println("Segunda línea");
        System.out.println("Tercera línea");

        // Para imprimir todo seguido, sin salto de línea
        System.out.print("Ahora esto ");
        System.out.print("Luego esto ");
        System.out.print("Luego lo de más allá");

        // Es posible insertar un salto de línea en cualquier sitio \n
        System.out.println("\n\nEstamos aquí\nY ahora estamos\nmás abajo");
    }
}
